-- 31. find consecutive visits 
WITH numbered AS (
    SELECT 
        patient_id,
        DATE(appointment_date) AS visit_date,
        ROW_NUMBER() OVER (
            PARTITION BY patient_id 
            ORDER BY DATE(appointment_date)
        ) AS rn
    FROM (
        SELECT DISTINCT patient_id, DATE(appointment_date) AS appointment_date
        FROM Appointments
    ) t
),
grouped AS (
    SELECT 
        patient_id,
        visit_date,
        visit_date - INTERVAL rn DAY AS grp
    FROM numbered
)
SELECT 
    patient_id,
    MIN(visit_date) AS start_date,
    MAX(visit_date) AS end_date,
    COUNT(*) AS consecutive_days
FROM grouped
GROUP BY patient_id, grp
HAVING CONSECUTIVE_DAYS >1
ORDER BY patient_id, start_date  ;

-- 32. For each patient, return their most recent appointment.

WITH RANKED AS 
