-- Rank doctors per specialization based on revenue
SELECT D.NAME,D.specialization,SUM(TREATMENT_COST) AS REVENUE,
 RANK() OVER (partition by D.specialization ORDER BY SUM(TREATMENT_COST) ) AS RANK1
