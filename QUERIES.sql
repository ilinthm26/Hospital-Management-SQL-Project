FROM APPOINTMENTS A INNER JOIN BILLS B ON A.PATIENT_ID = B.PATIENT_ID
GROUP BY A.PATIENT_ID
HAVING VISITS > 5 AND SPEND_AMOUNT > 20000
ORDER BY A.PATIENT_ID;

