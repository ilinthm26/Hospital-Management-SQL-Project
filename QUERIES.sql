FROM APPOINTMENTS) 
SELECT * 
FROM RANKED 
WHERE RNK = 1;

-- 33. Find Patients Who Missed Appointments (Gaps > 7 Days)

WITH GAP AS 
(SELECT PATIENT_ID, DATE(APPOINTMENT_DATE) AS CURR_DATE, 
LAG(DATE(APPOINTMENT_DATE)) OVER (PARTITION BY PATIENT_ID ORDER BY DATE(APPOINTMENT_DATE)) AS PREV_DATE
FROM APPOINTMENTS)
SELECT PATIENT_ID, DATEDIFF(CURR_DATE,PREV_DATE) AS GAP_DAYS
FROM GAP
WHERE DATEDIFF(CURR_DATE,PREV_DATE) >7;

-- 34. Show cumulative number of appointments for each patient over time.
WITH VISITS AS (
SELECT DISTINCT PATIENT_ID, DATE(APPOINTMENT_DATE), 
COUNT(*) OVER (partition by PATIENT_ID ORDER BY DATE(APPOINTMENT_DATE)
	ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS VISITS
FROM APPOINTMENTS
)
SELECT * 
FROM VISITS;

