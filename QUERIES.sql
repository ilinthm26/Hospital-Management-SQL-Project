-- 45. Top 2 Spending Patients per City
WITH SPENDING AS (
SELECT P.CITY,P.NAME, SUM(B.TOTAL_AMOUNT) AS TOT 
FROM PATIENTS P 
INNER JOIN BILLS B ON P.PATIENT_ID=B.PATIENT_ID
GROUP BY P.CITY, P.NAME),
RANKING AS ( SELECT *, DENSE_RANK() OVER (PARTITION BY CITY ORDER BY TOT DESC) AS RNK FROM SPENDING)
SELECT *, CASE 
WHEN TOT > 20000 THEN 'HIGH'
ELSE "LOW" END AS SPEND_RATE
FROM RANKING
WHERE RNK<=2;

